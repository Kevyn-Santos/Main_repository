let age = 15;
let carColor = "yellow";

//usando if
if(age >=18){
    console.log("Voce é maior de idade (if)");
}else{console.log("voce é menor de idade (if)");}

//usando ternario

age >= 18 ? console.log("Voce é maior de idade (TER)") : console.log("Voce é menor de idade (TER)");

//usando if
if(carColor == "yellow"){
    console.log("o carro é amarelo (IF)")
}else{console.log("O carro não é amarelo (IF)");}

//usando o ternario

carColor == "yellow" ? console.log("o carro é amarelo (TER)") : console.log("O carro não é amarelo (TER)");